//----------------------------------------------------------------------------------------------------
// getLocalDate
//----------------------------------------------------------------------------------------------------
export const getLocalDate = (date = new Date()) => new Date(date - (date.getTimezoneOffset() * 60000));

//----------------------------------------------------------------------------------------------------
// getDateStr
//----------------------------------------------------------------------------------------------------
export const getDateStr = (date = new Date()) => date.toISOString().substring(0, 10);

