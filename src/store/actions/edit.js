import ACTION from '../actionTypes';

//----------------------------------------------------------------------------------------------------
// openModalEditor
//----------------------------------------------------------------------------------------------------
export const openModalEditor = (content = undefined) => {
  return {
    type: ACTION._OPEN_MODAL_EDITOR,
    content,
  };
};
//----------------------------------------------------------------------------------------------------
// closeModalEditor
//----------------------------------------------------------------------------------------------------
export const closeModalEditor = () => {
  return {
    type: ACTION._CLOSE_MODAL_EDITOR,
  };
};
//----------------------------------------------------------------------------------------------------
// openMEmbeddedditor
//----------------------------------------------------------------------------------------------------
export const openEmbeddedEditor = (id) => {
  return {
    type: ACTION._OPEN_EMBEDDED_EDITOR,
    id,
  };
};
//----------------------------------------------------------------------------------------------------
// closeEmbeddedEditor
//----------------------------------------------------------------------------------------------------
export const closeEmbeddedEditor = () => {
  return {
    type: ACTION._CLOSE_EMBEDDED_EDITOR,
  };
};
